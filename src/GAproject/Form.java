    private void runActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_runActionPerformed
       int times=0;
			operations.pressed=false;
			int len=0;
			double exchange_c;
			double mutation_c;
			double inversion_c;
			double average=0;
			try{
			PrintWriter pw1=new PrintWriter("average.txt","UTF-8");
			PrintWriter pw2=new PrintWriter("global_min.txt","UTF-8");
		
			if(chromosome_list==null){
				chromosome_list=new ArrayList<Chromosome>();
				operations.createPopulation(chromosome_list,Integer.parseInt(chromosome_members.getText()),Integer.parseInt(chromosome_length.getText()));
			}
			try{
				times=Integer.parseInt(run_times.getText());
				if(members==0)
				members=Integer.parseInt(chromosome_members.getText());
				len=Integer.parseInt(chromosome_length.getText());
				exchange_c=Double.parseDouble(exchange_chance.getText());
				mutation_c=Double.parseDouble(mutation_chance.getText());
				inversion_c=Double.parseDouble(inversion_chance.getText());
			}catch(Exception ex){
				JOptionPane.showMessageDialog(null,"You should enter valid numbers");
				return;
			}
			

			chromosome_list=operations.createNewPopulation(chromosome_list, members, len, this);
			
			for(int i=0;i<times;i++){
				average=0;
				operations.Crossover(chromosome_list,members,len,this,exchange_c);
				operations.Mutation(chromosome_list,members,len,this,mutation_c);
				operations.GAInversion(chromosome_list, members, len, this, inversion_c);
				chromosome_list=operations.createNewPopulation(chromosome_list, members, len, this);
				double min=chromosome_list.get(0).value;
				for(int j=0;j<members;j++){
					average+=chromosome_list.get(j).value;
					if(chromosome_list.get(j).value<min)
						min=chromosome_list.get(j).value;
				}
				pw1.println(average/members);
				pw2.println(min);
				
			}
			pw1.close();
			pw2.close();
			}catch(Exception e){
				JOptionPane.showMessageDialog(null, "file not found");
			}
			
			
    }//GEN-LAST:event_runActionPerformed

	
	
	
